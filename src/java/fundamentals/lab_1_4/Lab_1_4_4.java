package java.fundamentals.lab_1_4;

public class Lab_1_4_4 {

    public static void main(String[] args) {

        int i = 42;

        System.out.println("i++ = " + i++);
        System.out.println("++i = " + ++i);

        int k = 42;

        System.out.println("k-- = " + k--);
        System.out.println("--k = " + --k);

    }

}
